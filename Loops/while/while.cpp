//while loop

#include<iostream>
using namespace std;
int main()
    
{
	int i=1;               //initialising the starting point
	while(i<=10)           //loop information
	{                            //curly braces start
		cout<<"hello\n";   //output message
		i++;               //loop direction
	}                            //curly braces end
	return 0;
}
