//for loop examples :- add numbers from 1 to 10

#include<iostream>
using namespace std;
int main()
    
{
	int i,n=0;                                   
	for(i=1;i<=10;i++)                             
	{         
	     n+=i;		                                    
	}  
	cout<<"\nThe sum of numbers from 1 to 10 is: "<<n;             
	return 0;
}
